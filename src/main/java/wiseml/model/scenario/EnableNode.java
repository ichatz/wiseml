package wiseml.model.scenario;

/**
 * This is a persistant class for the object enableNode that has the
 * properties of an enableNode. In the class there are
 * getter and setter methods for the properties.
 */

public class EnableNode {

    /**
     * id for object enableNode.
     */
    private int id;

    /**
     * this method returns the id of enableNode.
     *
     * @return id
     */
    public int getId() {
        return id;
    }

    /**
     * this method sets the id for the enableNode.
     *
     * @param id Integer id
     */
    public void setId(int id) {
        this.id = id;
    }
}
